package smartgraduates;

public class SwitchCase {
		public static void main(String[] args) {
			int num=2;
			
			switch (num) {
				case 1:
					System.out.println("This is JAN");
					break;
				
				case 2:
					System.out.println("This is FEB");
					break;
				
				case 3:
					System.out.println("This is MAR");
		
				default:
					System.out.println("Unknown");
				
		}
}}
